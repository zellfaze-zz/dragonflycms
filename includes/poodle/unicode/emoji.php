	protected static function aliases()
	{
		static $data;
		if (!$data) {
			$data = array();
			foreach (self::$emoji as $icon) {
				$data[":{$icon[1]}:"] = $icon[0];
			}
		}
		return $data;
	}

	public static function UTF8ToAliases($text)
	{
		$conv = self::aliases();
		return str_replace($conv, array_keys($conv), $text);
	}

	public static function aliasesToUTF8($text)
	{
		return strtr($text, self::aliases());
	}

	public static function getAliases()
	{
		$aliases = array();
		foreach (self::$emoji as $uni => $data) {
			$aliases[] = array($uni, $data[1] /*,$data[2]*/);
		}
		return json_encode($aliases, JSON_UNESCAPED_UNICODE);
	}

